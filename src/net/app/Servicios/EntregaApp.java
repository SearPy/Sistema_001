     private void retornarPresupuesto(){
        if(tablaped.getRowCount() > 0){
            if(tablaped.getSelectedRow() != -1){
                Entrega pe = cosdao.buscarPorId(Integer.parseInt(modelpres.getValueAt(tablaped.getSelectedRow(), 0).toString()));
                txtfecha.setText(retornarString(pe.getFecha()));
                txtusuario.setText(String.valueOf(pe.getUsuarioid().getNombre()));
                txtnro.setText(String.valueOf(pe.getId_pres()));
                Buscar_Presupuesto.dispose();
                botbuscar.setEnabled(false);
                txtestado.setText(pe.getEstado());
                txtnroproveedor.setText(String.valueOf(pe.getReg().getId_prov().getId_clie()));
                txtruc.setText(String.valueOf(pe.getReg().getId_prov().getRuc()));
                txtrazonsocial.setText(String.valueOf(pe.getReg().getId_prov().getRazon_social()));
                if(pe.getEstado().equals("ACTIVO")){
                    botanular.setEnabled(true);
                }else{
                    botanular.setEnabled(false);
                }
            }
        }
    }
     
    private void guardar() throws UnknownHostException{
            if(!txtusuario.getText().isEmpty()){  
                        if(JOptionPane.showConfirmDialog(null, "Desea guardar este registro,.", "Mensaje del Sistema.", JOptionPane.YES_OPTION) == 0){
                            Usuario us = udao.buscarPorNom(txtusuario.getText());
                            Servicios re = prodao.buscarPorId(Integer.parseInt(txtpedido.getText()));
                            Entrega pre = new Entrega(f,"ACTIVO", us,re);
                            cosdao.guardar(pre);
                            re.setEstado("ENTREGADO");
                            prodao.actualizar(re);
                            JOptionPane.showMessageDialog(null, "Guardado correctamente");
                            arranque();
                        }
                    
            }else{
                JOptionPane.showMessageDialog(null, "Inicie sesion");
            }
    }
    
    private void anular(){
        if(JOptionPane.showConfirmDialog(null, "Desea anular este registro,.", "Mensaje del Sistema.", JOptionPane.YES_OPTION) == 0){
            if(!txtnro.getText().isEmpty()){
                Servicios re = prodao.buscarPorId(Integer.parseInt(txtpedido.getText()));
                Entrega pre = cosdao.buscarPorId(Integer.parseInt(txtnro.getText()));
                if(pre.getEstado().equals("ACTIVO")){
                    pre.setEstado("ANULADO");
                    botanular.setEnabled(false);
                    cosdao.actualizar(pre);
                    re.setEstado("INFORMADO");
                    prodao.actualizar(re);
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se puede anular un presupuesto REALIZADO");
                }
            }
        }
    }
    
    
