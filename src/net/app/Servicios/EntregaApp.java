    private void retornarProduccion(){
        if(tablapres.getRowCount() > 0){
            if(tablapres.getSelectedRow() != -1){
            Servicios pe = prodao.buscarPorId(Integer.parseInt(modelpres.getValueAt(tablapres.getSelectedRow(), 0).toString()));
            txtfechapedido.setText(retornarString(pe.getFecha()));
            txtpedido.setText(String.valueOf(pe.getId_ord()));
            txtusuariopedido.setText(String.valueOf(pe.getId_us().getNombre()));
            txtnroproveedor.setText(String.valueOf(pe.getId_prov().getId_clie()));
            txtruc.setText(String.valueOf(pe.getId_prov().getRuc()));
            txtrazonsocial.setText(String.valueOf(pe.getId_prov().getRazon_social()));
            
            botnuevo.setEnabled(false);
            Buscar_Produccion.dispose();
            
            }
        }
    }
    
    private void tablaPresupuesto(){
        tablaped.getColumnModel().getColumn(0).setPreferredWidth(80);
        tablaped.getColumnModel().getColumn(1).setPreferredWidth(180);
        modelpres = (DefaultTableModel)tablaped.getModel();
        modelpres.setNumRows(0);
        List<Entrega> list = cosdao.list();
        for (Entrega l : list){
            if(fechaped.getText().equals("  /  /    ")){
               modelpres.addRow(new Object[]{
                l.getId_pres(), retornarString(l.getFecha())
            }); 
            }else{
               if(fechaped.getText().equals(retornarString(l.getFecha()))){
                   modelpres.addRow(new Object[]{
                l.getId_pres(), retornarString(l.getFecha())
            }); 
               }
            }
            
        }
    }
     
