    //m√©todo para cifrar el texto
    public static String cifradoCesar(String texto, int codigo) {
        StringBuilder cifrado = new StringBuilder();
        codigo = codigo % 26;
        for (int i = 0; i < texto.length(); i++) {
            if (texto.charAt(i) >= 'a' && texto.charAt(i) <= 'z') {
                if ((texto.charAt(i) + codigo) > 'z') {
                    cifrado.append((char) (texto.charAt(i) + codigo - 26));
                } else {
                    cifrado.append((char) (texto.charAt(i) + codigo));
                }
            } else if (texto.charAt(i) >= 'A' && texto.charAt(i) <= 'Z') {
                if ((texto.charAt(i) + codigo) > 'Z') {
                    cifrado.append((char) (texto.charAt(i) + codigo - 26));
                } else {
                    cifrado.append((char) (texto.charAt(i) + codigo));
                }
            }
        }
        return cifrado.toString();
    }

    //m√©todo para descifrar el texto
    public static String descifradoCesar(String texto, int codigo) {
        StringBuilder cifrado = new StringBuilder();
        codigo = 4 % 26;
        for (int i = 0; i < texto.length(); i++) {
            if (texto.charAt(i) >= 'a' && texto.charAt(i) <= 'z') {
                if ((texto.charAt(i) - codigo) < 'a') {
                    cifrado.append((char) (texto.charAt(i) - codigo + 26));
                } else {
                    cifrado.append((char) (texto.charAt(i) - codigo));
                }
            } else if (texto.charAt(i) >= 'A' && texto.charAt(i) <= 'Z') {
                if ((texto.charAt(i) - codigo) < 'A') {
                    cifrado.append((char) (texto.charAt(i) - codigo + 26));
                } else {
                    cifrado.append((char) (texto.charAt(i) - codigo));
                }
            }
        }
        return cifrado.toString();
    }
    
    
    
    private String hostname;
    private void arranque() throws UnknownHostException{
        List<datos_login> list = logdao.list();
        InetAddress localHost = InetAddress.getLocalHost();
        hostname = localHost.getHostName();
        if(list!=null){
            for(datos_login u: list){
                if(u.getEstado().equals(true) && hostname.equals(u.getMaquina())){
                    u.setEstado(false);
                    logdao.actualizar(u);
                }
            }
        }
        login.setEnabled(true);
        logaut.setEnabled(false);
        ayuda.setEnabled(true);
        referenciales.setEnabled(false);
        compras.setEnabled(false);
        ventasm.setEnabled(false);
        Recepcion.setEnabled(false);
        
    }
    
    public void iniciarSession(){
        java.util.Date fecha = new Date();
        String texto = txtpass.getText();
        try {
           String pass = new String(cifradoCesar(texto, 4));
           Usuario u= null;
           u =usdao.buscarPorNom(txtuser.getText());
           if(txtpass != null && pass.equals(u.getPass())){
                datos_login dl= new datos_login(u, fecha, true,hostname);
                logdao.guardar(dl);
                
                if(u.getRol().equals("admin")){
                    referenciales.setEnabled(true);
                    compras.setEnabled(true);
                    Recepcion.setEnabled(true);
                    ventasm.setEnabled(true);
                    
                    ayuda.setEnabled(true);
               }else {
                    if(u.getRol().equals("Compra")){
                    referenciales.setEnabled(true);
                    compras.setEnabled(true);
                    ayuda.setEnabled(true);
                    ventasm.setEnabled(true);
                    }else{
                        if(u.getRol().equals("Servicio")){
                        referenciales.setEnabled(true);
                        Recepcion.setEnabled(true);
                        ayuda.setEnabled(true);
                        ventasm.setEnabled(true);
                        }
                    }
               }
                
                login.setEnabled(false);
                logaut.setEnabled(true);
                txtuser.setText(null);
                txtpass.setText(null);
                Login.dispose();
                JOptionPane.showMessageDialog(null, "Sesi√≥n iniciada correctamente...");
            }
           else{
               JOptionPane.showMessageDialog(null, "Contrase√±a no coincide.");
            }
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Usuario: " + txtuser.getText() + " no econtrado.");           
        } 
    }

