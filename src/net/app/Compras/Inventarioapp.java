    private int indice;
    
    private int productoAgregado(int n){
        int b = 0;
            if(tabla.getRowCount() > 0){
                for (int i = 0; i < tabla.getRowCount(); i++) {
                    if(Integer.parseInt(model.getValueAt(i, 1).toString()) != n){
                        b = 0;
                    }else{
                        b = n;
                        indice = i;
                        break;
                    }
                }
            }
        return b;
    }
    
    private boolean esNum(String n){
        boolean b;
        try{
            int x = Integer.parseInt(n);
            b=true;
        }catch (Exception e){
            b=false;
        }
        return b;
    }
    
    private String can;
    private void agregar(){
        can = JOptionPane.showInputDialog("Ingrese Cantidad");
        if(esNum(can)){
            if(productoAgregado(Integer.parseInt(txtnroproducto.getText())) == 0){
                model.addRow(new Object[]{
                can,txtnroproducto.getText(),txtdescripcion.getText()
                });
                limpiar();
                
            }else{
                if(JOptionPane.showConfirmDialog(null, "Desea sumar la cantidad.", "Producto ya agredado", JOptionPane.YES_OPTION) == 0){
                   int x = Integer.parseInt(can) + Integer.parseInt(model.getValueAt(indice, 0 ).toString());
                   model.setValueAt(x, indice, 0);
                   limpiar();
                   
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cantidad debe ser un numero");
        }
    }
    
    private void removerProducto(){
        if(tabla.getRowCount() > 0){
            if(tabla.getSelectedRow() != -1){
                model.removeRow(tabla.getSelectedRow());
            }
        }
    }
    
    private void guardar() throws UnknownHostException{
        if(tabla.getRowCount() > 0){
            if(!txtusuario.getText().isEmpty()){
                if(JOptionPane.showConfirmDialog(null, "Desea guardar este registro,.", "Mensaje del Sistema.", JOptionPane.YES_OPTION) == 0){
                    Usuario us = udao.buscarPorNom(txtusuario.getText());
                    Inventario pe = new Inventario(f, "ACTIVO", us);
                    for (int i = 0; i < tabla.getRowCount(); i++) {
                        Det_inventario dt= new Det_inventario();
                        dt.setCantidad(Integer.parseInt(model.getValueAt(i, 0).toString()));
                        Producto p = pdao.buscarPorId(Integer.parseInt(model.getValueAt(i, 1).toString()));
                        dt.setId_prod(p);
                        pe.getDetalles().add(dt);
                        dt.setId_inv(pe);
                    }
                    pedao.guardar(pe);
                    JOptionPane.showMessageDialog(null, "Guardado correctamente");
                    arranque();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Inicie sesion");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Agregue un libro al carro de compras");
        }
    }
    
    private void anular(){
        if(JOptionPane.showConfirmDialog(null, "Desea anular este registro,.", "Mensaje del Sistema.", JOptionPane.YES_OPTION) == 0){
            if(!txtnro.getText().isEmpty()){
                Inventario pre = pedao.buscarPorId(Integer.parseInt(txtnro.getText()));
                if(pre.getEstado().equals("ACTIVO")){
                    pre.setEstado("ANULADO");
                    botanular.setEnabled(false);
                    pedao.actualizar(pre);
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se puede anular, El registro esta siendo utilizado en otra instancia");
                }
            }
        }
    }
    
    private void selecTable(){
        if(tabla.getRowCount()> 0){
            if(tabla.isEnabled()){
                if(tabla.getSelectedRow()> -1){
                   Producto p = pdao.buscarPorId(Integer.parseInt(model.getValueAt(tabla.getSelectedRow(),1).toString()));
                   txtnroproducto.setText(String.valueOf(p.getId_prod()));
                   txtdescripcion.setText(p.getDescripcion());
                }
            }
        }
    }
    
