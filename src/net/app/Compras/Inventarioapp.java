    private void retornarFactura(){
          if(tablaped.getRowCount() > 0){
            if(tablaped.getSelectedRow() != -1){
                Inventario pe = pedao.buscarPorId(Integer.parseInt(modelpe.getValueAt(tablaped.getSelectedRow(), 0).toString()));
                txtfecha.setText(retornarString(pe.getFecha()));
                txtusuario.setText(String.valueOf(pe.getUsuarioid().getNombre()));
                txtnro.setText(String.valueOf(pe.getId_inv()));
                List<Det_inventario> dt =pe.getDetalles();
                model.setNumRows(0);
                tabla.setEnabled(false);
                for (Det_inventario p : dt) {
                    model.addRow(new Object[]{
                        p.getCantidad(),p.getId_prod().getId_prod(),p.getId_prod().getDescripcion()
                    });
                } 
                txtestado.setText(pe.getEstado());
                if(txtestado.getText().equals("ACTIVO")){
                    botanular.setEnabled(true);
                }else{
                    botanular.setEnabled(false);
                }
                Buscar_Pedido.dispose();
            }
          }
    }
    
