    private void agregar(){
        
            if(productoAgregado(Integer.parseInt(txtnroproducto.getText())) == 0){
                can = JOptionPane.showInputDialog("Ingrese Cantidad");
                if(esNum(can)){
                    model.addRow(new Object[]{
                    can,txtnroproducto.getText(),txtdescripcion.getText(),txtprecio.getText(),Integer.parseInt(can)*Integer.parseInt(txtprecio.getText())
                    });
                    limpiar();
                    total();
                }else{
                    JOptionPane.showMessageDialog(null, "Cantidad debe ser un numero");
                }  
            }else{
                   int x = Integer.parseInt(model.getValueAt(indice, 0 ).toString());
                   int y = Integer.parseInt(txtprecio.getText()) * x;
                   int z = Integer.parseInt(txtprecio.getText());
                   model.setValueAt(z,indice,3);
                   model.setValueAt(y, indice, 4);
                   limpiar();
                   total();
            }
        
    }
    
