    private void Cobro(){
        int abc = Integer.parseInt(txtnro.getText());
        int tot = Integer.parseInt(txtmontocheque.getText())+ Integer.parseInt(txtmontotarjeta.getText())+ Integer.parseInt(txtefectivo.getText());
        int ch=caj.getCheque();
        int tj=caj.getTarjeta();
        int ef=caj.getEfectivo();
        if (tot == Integer.parseInt(txttotalcobro.getText())) {
            
            Cobros cob = new Cobros(txtnrocheque.getText(), txtbanco.getText(), Integer.parseInt(txtmontocheque.getText()),txtmarcatarjeta.getText(),txtcomprobante.getText(),
                    Integer.parseInt(txtmontotarjeta.getText()),Integer.parseInt(txtefectivo.getText()), abc);
            codao.actualizar(cob);
            caj.setCheque(ch + Integer.parseInt(txtmontocheque.getText()));
            caj.setTarjeta(tj + Integer.parseInt(txtmontotarjeta.getText()));
            caj.setEfectivo(ef + Integer.parseInt(txtefectivo.getText()));
            cadao.actualizar(caj);
            cobros.dispose();
            JOptionPane.showMessageDialog(null, "Cobro registrado con exito");
            
        }else{
            JOptionPane.showConfirmDialog(null, "El total cobrado debe ser igual al total de la factura");
        }
    }
    
    
