    private void total(){
        txttotal.setText(null);
        txttotal10.setText(null);
        txttotal5.setText(null);
        int tot=0;
        int tot10=0;
        int tot5=0;
        for (int i = 0; i < tabla.getRowCount(); i++) {
            Producto pro = pdao.buscarPorId(Integer.parseInt(model.getValueAt(i, 1).toString()));
            if(pro.getIva().equals("10%")){
                tot10 = tot10 + (Integer.parseInt(model.getValueAt(i, 4).toString()));
            }else{
                tot5 = tot5 + (Integer.parseInt(model.getValueAt(i, 4).toString()));
            }
            tot= tot + (Integer.parseInt(model.getValueAt(i, 4).toString()));
            
        }
        tot10 = tot10 / 11;
        tot5 = tot5 /  11;
        txttotal.setText(String.valueOf(tot));
        txttotal10.setText(String.valueOf(tot10));
        txttotal5.setText(String.valueOf(tot5));
    }
